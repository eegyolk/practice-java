package me.eguzman.learn._03_flow_control_statements;

public class Exercise10 {

	/**
	 * Write a program in Java that prints the squares of the first 30 natural numbers in the console.
	 */
	public static void main(String[] args) {
		for (int i = 1; i <= 30; i++) {
			System.out.printf("The square root of %d is %d %n", i, i * i);
		}

	}

}
