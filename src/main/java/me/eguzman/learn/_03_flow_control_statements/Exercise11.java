package me.eguzman.learn._03_flow_control_statements;

public class Exercise11 {

	/**
	 * Write a program in Java that prints 200 times the word "Hello."  Note: in your source code, you must only enter the word "Hello."
	 */
	public static void main(String[] args) {
		for (int i = 1; i <= 200; i++) {
			System.out.println(i + ". Hello");
		}

	}

}
